async function main() {
  for (const [index, style] of designStyles.entries()) {
    const [id, name, summary, keywords, palette, materials, lighting, negative, roomTips] = style;
    await prisma.designStyle.upsert({
      where: { id },
      update: { name, summary, keywords, palette, materials, lighting, negative, roomTips, sortOrder: index + 1 },
      create: { id, name, summary, keywords, palette, materials, lighting, negative, roomTips, sortOrder: index + 1 },
    });
  }
  console.log(`Seeded ${designStyles.length} design styles.`);
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
